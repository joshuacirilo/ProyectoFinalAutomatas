    private void graficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graficarActionPerformed
     //cambios        
    grafica vistaGrafica = new grafica();
    analizarCadenasControlador controladorCadenas = new analizarCadenasControlador(this, vistaGrafica);
    vistaGrafica.setControlador(controladorCadenas); // 🔹 conecta la vista con el controlador
    controladorCadenas.ingresarDatos();
    vistaGrafica.setVisible(true);
    
    // Obtener el autómata desde el otro controlador 
    mostrarDatosControlador controladorDatos = new mostrarDatosControlador(this);
    controladorDatos.ingresarDatos();
    Automata automata = controladorDatos.getAutomata();

    if (automata == null) {
        JOptionPane.showMessageDialog(this, "No hay un autómata cargado.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }


    // Crear el controlador de automata
    AutomataControlador controladorAutomata = new AutomataControlador(automata);

    // Definir rutas (puedes cambiarlas)
    String pathDot = "C:/Program Files/Graphviz/bin/dot.exe";
    String rutaEntrada = "C:/AutomataFinito/tmp/entradaGraphviz.txt";
    String rutaSalida = "C:/AutomataFinito/tmp/salidaAutomata.png";

    // Llamar al método que genera la imagen
                // 🔹 Supongamos que el automata tiene un método getEstadoActual()
            Set<String> estadosActivos = new java.util.HashSet<>();
            estadosActivos.add(automata.getEstadoActual());
    controladorAutomata.generarImagenAutomata(pathDot, rutaEntrada, rutaSalida, vistaGrafica.getLabelImagen(), estadosActivos);
    
///hizimos un cambio aca
    controladorCadenas = new analizarCadenasControlador(this, vistaGrafica);
    controladorCadenas.setAutomata(automata);
    controladorCadenas.ingresarDatos();
    vistaGrafica.setControlador(controladorCadenas);
 
    
    }//GEN-LAST:event_graficarActionPerformed

