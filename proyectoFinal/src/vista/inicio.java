    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
       mostrarDatosControlador eje = new mostrarDatosControlador(this);
       eje.limpiarVista();
        JFileChooser fileChooser = new JFileChooser();
        int resultado = fileChooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();
            try {
                // Crear instancia del controlador y usarlo
                controlador.abrirControlador controladorAbrir = new controlador.abrirControlador();
                String contenido = controladorAbrir.abrirArchivo(archivoSeleccionado);
                setTexto(contenido);  // Mostrar en el JTextArea
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this,
                        "Error al leer el archivo: " + ex.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_abrirActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int resultado = fileChooser.showSaveDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();
            try {
                controlador.abrirControlador controlador = new controlador.abrirControlador();
                String contenido = getTexto(); // asegúrate que existe getTexto()
                controlador.guardarArchivo(archivoSeleccionado, contenido);
                JOptionPane.showMessageDialog(this, "Archivo guardado exitosamente.");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this,
                        "Error al guardar el archivo: " + ex.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_guardarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de que quieres borrar el contenido actual?",
                "Confirmar nuevo documento",
                JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Limpiar el JTextArea
            setTexto("");

            // Limpiar el estado inicial
            getEstadoInicial().setText("");

            // Limpiar tabla de aceptación
            javax.swing.table.DefaultTableModel modeloAceptacion
                    = (javax.swing.table.DefaultTableModel) getTablaAceptacion1().getModel();
            modeloAceptacion.setRowCount(0);

            // Limpiar tabla de símbolos
            javax.swing.table.DefaultTableModel modeloSimbolos
                    = (javax.swing.table.DefaultTableModel) getTablasimbolos1().getModel();
            modeloSimbolos.setRowCount(0);

            // Limpiar tabla de transiciones
            javax.swing.table.DefaultTableModel modeloTransiciones
                    = (javax.swing.table.DefaultTableModel) getTablaTransicciones().getModel();
            modeloTransiciones.setRowCount(0);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

