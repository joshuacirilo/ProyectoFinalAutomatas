    /**
     * üî• Elimina todos los archivos .png dentro de la carpeta
     * C:/AutomataFinito/tmp
     */
    private void borrarImagenesGeneradas() {
        String ruta = "C:/AutomataFinito/tmp";
        File carpeta = new File(ruta);

        if (!carpeta.exists() || !carpeta.isDirectory()) {
            System.out.println("‚ö† La carpeta no existe o no es un directorio: " + ruta);
            return;
        }

        File[] archivos = carpeta.listFiles((dir, nombre) -> nombre.toLowerCase().endsWith(".png"));
        if (archivos == null || archivos.length == 0) {
            System.out.println("‚Ñπ No hay im√°genes para eliminar en: " + ruta);
            return;
        }

        int eliminados = 0;
        for (File archivo : archivos) {
            if (archivo.delete()) {
                eliminados++;
                System.out.println("üóë Imagen eliminada: " + archivo.getName());
            } else {
                System.err.println("‚ùå No se pudo eliminar: " + archivo.getName());
            }
        }

        System.out.println("‚úÖ Se eliminaron " + eliminados + " im√°genes .png de la carpeta tmp.");
    }//GEN-LAST:event_regresarPaginaActionPerformed

